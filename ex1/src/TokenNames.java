public interface TokenNames {
    /* terminals */
    public static final int EOF = 0;
    public static final int PLUS = 1;           // +
    public static final int MINUS = 2;          // -
    public static final int TIMES = 3;          // *
    public static final int DIVIDE = 4;         // /
    public static final int LPAREN = 5;         // (
    public static final int RPAREN = 6;         // )
    public static final int INT = 7;            // integer
    public static final int ID = 8;             // identifier
    public static final int CLASS = 9;          // class keyword
    public static final int ARRAY = 10;         // array keyword
    public static final int EXTENDS = 11;       // extends keyword
    public static final int RETURN = 12;        // return keyword
    public static final int WHILE = 13;         // while keyword
    public static final int IF = 14;            // if keyword
    public static final int NEW = 15;           // new keyword
    public static final int STRING = 16;        // string
    public static final int TYPE_INT = 17;      // int type keyword
    public static final int TYPE_STRING = 18;   // string type keyword
    public static final int LBRACK = 19;        // [
    public static final int RBRACK = 20;        // ]
    public static final int LBRACE = 21;        // {
    public static final int RBRACE = 22;        // }
    public static final int NIL = 23;           // nil keyword
    public static final int COMMA = 24;         // ,
    public static final int DOT = 25;           // .
    public static final int SEMICOLON = 26;     // ;
    public static final int TYPE_VOID = 27;     // void type keyword
    public static final int ASSIGN = 28;        // :=
    public static final int EQ = 29;            // =
    public static final int LT = 30;            // <
    public static final int GT = 31;            // >
    public static final int ERROR = 32;         // any lexical error
    
}
